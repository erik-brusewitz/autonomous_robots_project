#include "cluon-complete.hpp"
#include "opendlv-standard-message-set.hpp"

#include <opencv2/highgui/highgui.hpp>
#include <opencv2/imgproc/imgproc.hpp>

#include <cstdint>
#include <iostream>
#include <memory>
#include <mutex>
#include <vector>
#include <string>
#include <chrono>
#include <thread>
#include <filesystem>
#include <unistd.h>

std::string WINDOW_NAME = "Frame";

std::string HOME(getenv("HOME"));
std::string SAVING_DIR = HOME + "/extracted_kiwi_imgs/";


int32_t main(int32_t argc, char **argv) {
    int32_t retCode{1};
    auto commandlineArguments = cluon::getCommandlineArguments(argc, argv);
    if ( (0 == commandlineArguments.count("cid")) ||
         (0 == commandlineArguments.count("name")) ||
         (0 == commandlineArguments.count("width")) ||
         (0 == commandlineArguments.count("height")) ) {
        std::cerr << argv[0] << " attaches to a shared memory area containing an ARGB image." << std::endl;
        std::cerr << "Usage:   " << argv[0] << " --cid=<OD4 session> --name=<name of shared memory area> [--verbose]" << std::endl;
        std::cerr << "         --cid:    CID of the OD4Session to send and receive messages" << std::endl;
        std::cerr << "         --name:   name of the shared memory area to attach" << std::endl;
        std::cerr << "         --width:  width of the frame" << std::endl;
        std::cerr << "         --height: height of the frame" << std::endl;
        std::cerr << "Example: " << argv[0] << " --cid=112 --name=img.argb --width=640 --height=480 --verbose" << std::endl;
    }
    else {
        const std::string NAME{commandlineArguments["name"]};
        const uint32_t WIDTH{static_cast<uint32_t>(std::stoi(commandlineArguments["width"]))};
        const uint32_t HEIGHT{static_cast<uint32_t>(std::stoi(commandlineArguments["height"]))};
        const bool VERBOSE{commandlineArguments.count("verbose") != 0};
        const uint32_t DELAY_MILLISECONDS = (commandlineArguments.count("delay") != 0) ? static_cast<uint32_t>(std::stoi(commandlineArguments["delay"])) : 1000;
        const std::string IMG_PREFIX = (commandlineArguments.count("prefix") != 0) ? commandlineArguments["prefix"] : "img";

        // Attach to the shared memory.
        std::unique_ptr<cluon::SharedMemory> sharedMemory{new cluon::SharedMemory{NAME}};
        if (sharedMemory && sharedMemory->valid()) {
            std::clog << argv[0] << ": Attached to shared memory '" << sharedMemory->name() << " (" << sharedMemory->size() << " bytes)." << std::endl;

            // Interface to a running OpenDaVINCI session; here, you can send and receive messages.
            cluon::OD4Session od4{static_cast<uint16_t>(std::stoi(commandlineArguments["cid"]))};

            if (VERBOSE) {cv::namedWindow(WINDOW_NAME, 1);}

            int counter = 0;
            while (od4.isRunning()) {
                cv::Mat img;

                // Wait for a notification of a new frame.
                sharedMemory->wait();

                // Lock the shared memory.
                sharedMemory->lock();
                {
                    // Copy image into cvMat structure.
                    // Be aware of that any code between lock/unlock is blocking
                    // the camera to provide the next frame. Thus, any
                    // computationally heavy algorithms should be placed outside
                    // lock/unlock
                    cv::Mat wrapped(HEIGHT, WIDTH, CV_8UC4, sharedMemory->data());
                    img = wrapped.clone();
                }
                sharedMemory->unlock();
                // "/home/arashito/Pictures/imgs/Gray_Image.png"

                bool check = cv::imwrite(SAVING_DIR+IMG_PREFIX+std::to_string(counter)+".png", img);
        
                // Display image.
                if (VERBOSE) {
                    if (check == false) {std::cout << "Mission - Saving the image, FAILED" << std::endl;}
                    else {std::cout << "Successfully saved the image. " << std::endl;}
                    cv::imshow(WINDOW_NAME, img);
                    cv::waitKey(1);
                }

                counter++;

                std::this_thread::sleep_for(std::chrono::milliseconds(DELAY_MILLISECONDS));
            }
        }
        retCode = 0;
    }
    return retCode;
}

